    private void mostrarTexto() {
        if (jRadioButtonNegro.isSelected()) {
            jTextArea.setForeground(Color.BLACK);
        } else {
            jTextArea.setForeground(Color.WHITE);
        }
        if (jRadioButtonVerde.isSelected()) {
            jTextArea.setBackground(Color.GREEN);
        } else if (jRadioButtonRojo.isSelected()) {
                jTextArea.setBackground(Color.RED);
        } else {
            jTextArea.setBackground(Color.BLUE);
        }
        if (jCheckBoxNegrita.isSelected() && jCheckBoxCursiva.isSelected()) {
            jTextArea.setFont(new java.awt.Font("Segoe UI", Font.BOLD | Font.ITALIC, 18));
        } else if (jCheckBoxNegrita.isSelected()) {
            jTextArea.setFont(new java.awt.Font("Segoe UI", Font.BOLD, 18));
        } else if (jCheckBoxCursiva.isSelected()) {
            jTextArea.setFont(new java.awt.Font("Segoe UI", Font.ITALIC, 18)); // NOI18N
        } else {
            jTextArea.setFont(new java.awt.Font("Segoe UI", Font.PLAIN, 18)); // NOI18N
        }
        jTextArea.setText(jTextFieldMostrar.getText());
    }

