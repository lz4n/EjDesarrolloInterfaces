    private void convertirUnidades(JTextField campoOrigen, JTextField campoResultado, double equivalencia) {
        try {
            campoResultado.setText(String.valueOf(Double.parseDouble(campoOrigen.getText()) * equivalencia));
        } catch (NumberFormatException ignore) {

        }
    }

    private void checkNumeroDecimal(KeyEvent e, JTextField textField) {
        char caracter = e.getKeyChar();
        if (!((Character.isDigit(caracter) || (caracter == '.') || caracter == KeyEvent.VK_BACK_SPACE || caracter == KeyEvent.VK_DELETE) &&
                (textField.getText().isEmpty() || (caracter != '.' || textField.getText().indexOf('.') == -1)))) {
            e.consume();
        }
    }

